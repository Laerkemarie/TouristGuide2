package miniOpgaver.NummerEt;

public class Dog {
    private String name;
    private int age;

    public Dog(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name = name;
    }

    public int getAge() {
        return age = age;
    }

    public int getAgeInDogYears() {
        return age * 7;
    }
}
